// while loop is used to repeat a block of code as long as a specified condition is true.

let i = 0;
while (i < 10) {
  console.log("while loop ");
  i++;
  
}

// difference between for loop and while loop
// for loop is used when the number of iterations is known
// while loop is used when the number of iterations is not known
// for loop is more concise and easier to read
// while loop is more flexible and can be used in more situations